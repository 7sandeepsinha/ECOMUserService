CREATE TABLE ecom_user
(
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    email    VARCHAR(255),
    password VARCHAR(255),
    CONSTRAINT pk_ecom_user PRIMARY KEY (id)
);

CREATE TABLE ecom_user_roles
(
    ecom_user_id BIGINT NOT NULL,
    roles_id     BIGINT NOT NULL,
    CONSTRAINT pk_ecom_user_roles PRIMARY KEY (ecom_user_id, roles_id)
);

CREATE TABLE role
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    role VARCHAR(255),
    CONSTRAINT pk_role PRIMARY KEY (id)
);

CREATE TABLE session
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    token          VARCHAR(255),
    expiring_at    TIMESTAMP WITHOUT TIME ZONE,
    login_at       TIMESTAMP WITHOUT TIME ZONE,
    user_id        BIGINT,
    session_status SMALLINT,
    CONSTRAINT pk_session PRIMARY KEY (id)
);

